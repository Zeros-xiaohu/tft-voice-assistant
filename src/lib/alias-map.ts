// ─── 预处理函数 ───

/**
 * 转义正则表达式中的特殊字符。
 */
function escapeRegex(str: string): string {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

/**
 * 对用户原始输入进行别名 → 标准名替换。
 *
 * 替换策略：
 * 1. 先替换长别名（如「巨人杀手」），再替换短别名（如「巨杀」），避免截断
 * 2. 不区分大小写（蓝buff / 蓝BUFF 都能匹配）
 * 3. 别名与标准名相同时跳过
 *
 * @param raw 用户原始输入文本
 * @returns 替换别名后的文本，直接送给 State Parser
 */
export function preprocessInput(raw: string): string {
  if (!raw || !raw.trim()) return ''

  let processed = raw.trim()

  const allAliases: Record<string, string> = {
    ...HERO_ALIAS,
    ...ITEM_ALIAS,
  }

  // 按别名长度降序排列：长的先替换
  const sortedKeys = Object.keys(allAliases).sort(
    (a, b) => b.length - a.length
  )

  for (const alias of sortedKeys) {
    const standard = allAliases[alias]
    if (alias === standard) continue
    const escapedAlias = escapeRegex(alias)
    const regex = new RegExp(escapedAlias, 'gi')
    processed = processed.replace(regex, standard)
  }

  return processed
}
